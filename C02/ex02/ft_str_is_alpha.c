int	ft_str_is_alpha(char *str)
{
	int i;

	i = 1;
	while (*str)
	{
		if (*str < 'A' || *str > 'z')
		{
			i = 0;
		}
		else if (*str > 'Z' && *str < 'a')
		{
			i = 0;
		}
		str++;
	}
	return (i);
}
