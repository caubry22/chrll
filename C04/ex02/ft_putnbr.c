#include <unistd.h>

void	ft_putnbr(int nb)
{
	unsigned int	neg;
	char			a;

	neg = nb;
	if (nb < 0)
	{
		neg = -nb;
		write(1, "-", 1);
	}
	a = neg % 10 + '0';
	if (neg > 9)
	{
		ft_putnbr(neg / 10);
		write(1, &a, 1);
	}
	else
	{
		write(1, &a, 1);
	}
}
