#include <unistd.h>

int	ft_verif(char *str)
{
	int i;

	i = 0;
	while (i <= 30)
	{
		if (str[i] < '1' || str[i] > '4')
			return (0);
		if (str[i + 1] && str[i + 1] != ' ')
			return (0);
		i = i + 2;
	}
	if (str[i - 1])
		return (0);
	return (1);
}
