#include <stdlib.h>

int	ft_conv(char *str, int *tab)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (str[i])
	{
		tab[j] = str[i] - 48;
		i = i + 2;
		j++;
	}
	return (*tab);
}
