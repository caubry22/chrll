int	ft_somme(int a, int b)
{
	return (a + b);
}

int	ft_soustraction(int a, int b)
{
	return (a - b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_multi(int a, int b)
{
	return (a * b);
}

int	ft_mod(int a, int b)
{
	return (a % b);
}
